    /**
     * @var Place[]
     */
    public $places = [];
    function __construct($zipCode, $country = 'us')
    {
        switch (array_key_exists($zipCode,$this->apoFpoDpo )){
            case true:
                $dataArray = $this->apoFpoDpo[$zipCode];
                $this->postCode = $zipCode;
                $this->country = 'United States';
                $this->country = 'US';
                $this->places[0] = new Place();
                $this->places[0]->state = $dataArray['state'];
                $this->places[0]->stateAbbreviation = $dataArray['state'];
                break;
            default:
                $responseObject = $this->lookup($zipCode, $country);

                $prop = 'post code';
                $this->postCode = $responseObject->$prop;
                $this->country = $responseObject->country;
                $prop = 'country abbreviation';
                $this->countryAbbreviation = $responseObject->$prop;

                for ($i = 0, $c = count($responseObject->places); $i<$c;$i++){
                    array_push($this->places, Place::createFromJsonObject($responseObject->places[$i]));
                }
                break;
        }

    }

    protected function lookup($zipCode, $country = 'us'){
        $client = new Client();

        $country = $this->getCountryCode($country);

        $result = $client->request('GET','http://api.zippopotam.us/'.$country.'/'.$zipCode);

        return \GuzzleHttp\json_decode($result->getBody()->__toString());
    }

